	SpsFuaEnd := []byte{0x7C, 0x47, 0x1E, 0x30, 0x63, 0x2C}

	assert.Equal(true, spreader.Spreading, "should be spreading")
	assert.Equal(uint16(1), spreader.RTPOffset, "Bad offset")

